export function toScheduled(ex: ExerciseDef, sets: number, reps: string, restSec: number): ScheduledExercise {
  return {
    id: ex.id,
    name: ex.name,
    sets,
    reps,
    restSec,
    videoUrl: ex.videoUrl,
    difficulty: ex.difficulty,
    equipment: ex.equipment,
  };
}

